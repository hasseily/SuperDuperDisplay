/**
 * Generated tables that return X pixel start and Y pixel row for a given HGR memory address.
 * Courtesy of Michael Pohoreski
 * https ://github.com/Michaelangel007
 * 
 * To generate, run this javascript:
 
	 var a, x, y, hgr = [], HGR_ADDR2X = new Array( 16384 ), HGR_ADDR2Y = new Array( 16384 );

	HGR_ADDR2X.fill( 0, 0x0000, 0x4000 );
	HGR_ADDR2Y.fill( 0, 0x0000, 0x4000 );

	// Make tables
	for( y = 0; y < 192; ++y )
	{
		for( x = 0; x < 280; x += 7 )
		{
			a = 0x2000 + ((y/64)|0)*0x28 + ((y%8)|0)*0x400 + ((y/8)&7)*0x80;
			HGR_ADDR2Y[ a+x/7 ] = y;
			HGR_ADDR2X[ a+x/7 ] = x;
		}
	}

	function dumpTable( array, name )
	{
		var text = "const int " + name + "[] =\n{";
		for( a = 0x2000; a < 0x4000; a += 16 )
		{
			text += "\n    ";
			for( var i = 0; i < 16; ++i )
				text += ("   "+array[a+i]).slice(-3)
					 + (((a == 0x3FF0) && (i == 15)) ? " " : ",");
			text += "   // [$" + ("0000"+a.toString(16).toUpperCase()).slice(-4) + "]";
		}
		text += "\n};\n";
		console.log( text );
	}

	dumpTable( HGR_ADDR2X, "HGR_ADDR2X" );
	dumpTable( HGR_ADDR2Y, "HGR_ADDR2Y" );

 */


